<div id='SettlementsSec' class='Sec2'>
    <p>Humans naturally seek to band together. A successful Base, strategically placed with strong fortifications, can grow over time into a Settlement. Large numbers of survivors can congregate in Settlements and begin to rebuild civilization. This requires the cooperation of many different people with varying agendas and therefore some form of organizational system must be developed.</p>
    <button id='PoliticsBtn' class='Btn'>POLITICS</button>
    <div id='PoliticsSec' class='Sec3'>
        <p>Humans often come into conflict with each other. When the conflict is serious, it could threaten an entire Settlement. All Settlements have some set of laws which are prohibitions on certain behavior and consequences for ignoring those prohibitions. Whatever power (formal or de facto) determines and enforces these laws is a government. There are three basic forms of government: autocracy, oligarchy, and democracy.</p>
