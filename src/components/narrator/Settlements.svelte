        <button id='OligarchyBtn' class='Btn'>OLIGARCHY</button>
        <div id='OligarchySec' class='Sec4'>
            <p>This is a small group of elites who share power and formalize a set of laws to run the Settlement according to bureaucratic process. They may or may not be elected. They often have power and interests of their own.</p>
        </div>
        <button id='DemocracyBtn' class='Btn'>DEMOCRACY</button>
        <div id='DemocracySec' class='Sec4'>
            <p>This is a minimal government in which the people collectively decide major issues. Small issues and specialized functions may be delegated to representative groups. This is usually the slowest form of government.</p>
        </div>
        <p>The path a Settlement takes involves many factors including the personalities of influential people, availability of resources, and the culture of the populace. Any system on such a small scale is very fragile. A few determined and organized people could theoretically overthrow any existing system and set up their own. In any case, the responsibility assumed by any leader is enormous.</p>
    </div>
